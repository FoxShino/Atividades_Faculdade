public class atividade1 {
    public static void main(String[] args) throws Exception {

        int normal, estudantes, acessivel, resultado;

        normal = 100;
        estudantes = 60;
        acessivel = 40;

        resultado = normal + estudantes + acessivel;

        System.out.println("Ingressos normais: " + normal);
        System.out.println("Ingressos estudantes: " + estudantes);
        System.out.println("Ingressos acessiveis: " + acessivel);
        System.out.println("Ingressos totais: " + resultado);
    }

}